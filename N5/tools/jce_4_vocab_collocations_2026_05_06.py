def main():
    with VOCAB.open('r', encoding='utf-8') as f:
        data = json.load(f)

    entries = data['entries']

    # Match by `form` first; on multi-form keys (like 'いる#2') try
    # by-form-then-disambiguate-by-pos.
    matched = 0
    skipped_have = 0
    by_form_unique: dict[str, list[dict]] = {}
    for e in entries:
        by_form_unique.setdefault(e.get('form', ''), []).append(e)

    not_found = []
    for key, collocs in COLLOCATIONS.items():
        # Strip a trailing '#N' if present (homograph disambiguator;
        # we ignore the suffix and match the first non-already-populated
        # entry of that form)
        form_only = key.split('#', 1)[0]
        candidates = by_form_unique.get(form_only)
        if not candidates:
            not_found.append(key)
            continue
        # Pick the first candidate without collocations
        target = None
        for c in candidates:
            if not c.get('collocations'):
                target = c
                break
        if target is None:
            skipped_have += 1
            continue
        target['collocations'] = collocs
        target['collocations_provenance'] = 'llm_curated'
        matched += 1

    print(f'Authored collocations on {matched} entries.')
    print(f'Skipped (already had collocations): {skipped_have}')
    if not_found:
        print(f'Forms not found: {not_found[:10]} (showing first 10 of {len(not_found)})')

    with VOCAB.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'Wrote: {VOCAB}')


if __name__ == '__main__':
    main()
