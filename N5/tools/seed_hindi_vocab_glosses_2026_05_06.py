def lookup_hindi(form: str, gloss: str) -> str | None:
    """Find a Hindi translation for the given form, using gloss-substring
    disambiguation when the form is a homograph (value is a list of tuples).
    """
    val = HINDI_BY_FORM.get(form)
    if val is None:
        return None
    if isinstance(val, str):
        return val
    # List of (hint, hindi) tuples — pick the first whose hint appears in gloss
    if isinstance(val, list):
        gloss_lower = (gloss or '').lower()
        for hint, hindi in val:
            if hint.lower() in gloss_lower:
                return hindi
        # Fall through to first option as fallback
        return val[0][1] if val else None
    return None


def main():
    with VOCAB.open('r', encoding='utf-8') as f:
        data = json.load(f)

    entries = data['entries']
    matched = 0
    skipped_have_hi = 0
    unmatched = []

    for entry in entries:
        existing = entry.get('gloss_hi')
        if existing:
            skipped_have_hi += 1
            continue
        form = entry.get('form', '')
        gloss = entry.get('gloss', '')
        hindi = lookup_hindi(form, gloss)
        if hindi is None:
            unmatched.append((entry.get('id'), form, gloss[:60]))
            continue
        entry['gloss_hi'] = hindi
        prov = entry.get('gloss_provenance') or {}
        if isinstance(prov, dict):
            prov['hi'] = 'llm_curated'
            entry['gloss_provenance'] = prov
        matched += 1

    print(f'Matched and seeded: {matched}')
    print(f'Already had gloss_hi (skipped): {skipped_have_hi}')
    print(f'Unmatched: {len(unmatched)}')
    if unmatched:
        # Encode safely for cp932 console
        import sys, io
        sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8', errors='replace')
        print('First 30 unmatched:')
        for uid, f, g in unmatched[:30]:
            print(f'  {uid:<70}  form={f}  gloss={g}')

    with VOCAB.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'Wrote: {VOCAB}')


if __name__ == '__main__':
    main()
