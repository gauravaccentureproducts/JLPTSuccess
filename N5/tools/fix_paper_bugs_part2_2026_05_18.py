def backup(fp):
    bak = fp + f".bak_{TODAY}_paper_004_proper"
    if not os.path.exists(bak):
        shutil.copy2(fp, bak)
    return bak

def main():
    files = sorted(glob.glob(os.path.join(REPO_N5, "data", "papers", "**", "*.json"), recursive=True))
    total_changes = 0
    for fp in files:
        if ".bak" in fp:
            continue
        with open(fp, "r", encoding="utf-8") as f:
            data = json.load(f)
        modified = False
        changes_this_file = []
        for q in data.get("questions", []):
            qid = q.get("id", "")
            if qid in REWRITES:
                new_rh = REWRITES[qid]
                if q.get("rationale_hi") != new_rh:
                    q["rationale_hi"] = new_rh
                    q["rationale_hi_provenance"] = PROVENANCE
                    changes_this_file.append(qid)
                    modified = True
                    total_changes += 1
        if modified:
            backup(fp)
            with open(fp, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            print(f"WROTE {os.path.basename(fp)} — {len(changes_this_file)} rewrites: {', '.join(changes_this_file)}")
    print()
    print(f"Total rationale_hi rewrites: {total_changes}")

if __name__ == "__main__":
    main()
