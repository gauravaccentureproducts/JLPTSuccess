def main() -> int:
    # Vocab pass
    vdata = json.loads(VF.read_text(encoding='utf-8'))
    by_form_reading = dict(VOCAB_HI)
    by_reading = {}
    for (form, reading), hi in VOCAB_HI.items():
        if form == reading:
            by_reading[reading] = hi
    n_vocab = 0
    for e in vdata.get('entries', []):
        form = e.get('form')
        reading = e.get('reading')
        hi = by_form_reading.get((form, reading)) or by_reading.get(reading)
        if hi is None:
            continue
        if e.get('gloss_hi') != hi:
            e['gloss_hi'] = hi
            # mark provenance
            if not e.get('gloss_provenance'):
                e['gloss_provenance'] = 'machine_translated'
            n_vocab += 1
    VF.write_text(json.dumps(vdata, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')

    # Kanji pass
    kdata = json.loads(KF.read_text(encoding='utf-8'))
    n_kanji = 0
    for e in kdata.get('entries', []):
        glyph = e.get('glyph')
        if glyph in KANJI_HI:
            new = KANJI_HI[glyph]
            if e.get('meanings_hi') != [new] and e.get('meanings_hi') != new:
                e['meanings_hi'] = [new]
                if not e.get('meanings_provenance'):
                    e['meanings_provenance'] = 'machine_translated'
                n_kanji += 1
    KF.write_text(json.dumps(kdata, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')

    total_v = len(vdata['entries'])
    total_k = len(kdata['entries'])
    n_v_total = sum(1 for e in vdata['entries'] if e.get('gloss_hi'))
    n_k_total = sum(1 for e in kdata['entries'] if e.get('meanings_hi'))
    print(f'Vocab gloss_hi: applied {n_vocab} writes (now {n_v_total}/{total_v} translated)')
    print(f'Kanji meanings_hi: applied {n_kanji} writes (now {n_k_total}/{total_k} translated)')
    return 0


if __name__ == '__main__':
    sys.exit(main())
