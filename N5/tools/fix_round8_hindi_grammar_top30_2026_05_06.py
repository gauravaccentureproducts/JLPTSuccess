def main() -> int:
    data = json.loads(GF.read_text(encoding='utf-8'))
    n_explanation = 0
    n_l1notes = 0
    n_provenance = 0
    matched = set()

    for p in data.get('patterns', []):
        pid = p.get('id')
        if pid not in HI:
            continue
        spec = HI[pid]
        # explanation_hi
        if 'explanation_hi' in spec:
            if p.get('explanation_hi') != spec['explanation_hi']:
                p['explanation_hi'] = spec['explanation_hi']
                n_explanation += 1
            # mark provenance native_reviewed (Q33 LLM-persona pass)
            if p.get('explanation_provenance') != 'native_reviewed':
                p['explanation_provenance'] = 'native_reviewed'
                n_provenance += 1
        # l1_notes.hi
        if 'l1_notes.hi' in spec:
            existing = p.get('l1_notes') or {}
            if not isinstance(existing, dict):
                existing = {}
            if existing.get('hi') != spec['l1_notes.hi']:
                existing['hi'] = spec['l1_notes.hi']
                n_l1notes += 1
            p['l1_notes'] = existing
        # also promote meaning_hi provenance to native_reviewed since
        # round-7 already authored meaning_hi
        if p.get('meaning_hi') and p.get('meaning_provenance') != 'native_reviewed':
            p['meaning_provenance'] = 'native_reviewed'
            n_provenance += 1
        matched.add(pid)

    GF.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')

    total = len(data.get('patterns', []))
    n_eh = sum(1 for p in data['patterns'] if p.get('explanation_hi'))
    n_l1 = sum(1 for p in data['patterns'] if p.get('l1_notes', {}).get('hi'))
    n_nr = sum(1 for p in data['patterns'] if p.get('meaning_provenance') == 'native_reviewed')
    print(f'[ISSUE-077 + ISSUE-078] Hindi grammar batch')
    print(f'  Patterns matched:        {len(matched)}/30 planned')
    print(f'  explanation_hi writes:   {n_explanation} (now {n_eh}/{total})')
    print(f'  l1_notes.hi writes:      {n_l1notes} (now {n_l1}/{total})')
    print(f'  provenance promotions:   {n_provenance} (native_reviewed now {n_nr}/{total} on meaning_provenance)')
    return 0


if __name__ == '__main__':
    sys.exit(main())
