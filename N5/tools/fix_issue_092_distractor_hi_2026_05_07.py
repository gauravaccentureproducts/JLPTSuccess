def main():
    with QUESTIONS.open('r', encoding='utf-8') as f:
        data = json.load(f)
    qs = data['questions']

    matched_specific = 0
    matched_generic = 0
    skipped = 0

    for q in qs:
        if q.get('distractor_explanations_hi'):
            skipped += 1
            continue

        en_distractors = q.get('distractor_explanations')
        if not en_distractors:
            continue

        qid = q.get('id')
        if qid in HINDI_DISTRACTORS:
            # Use the hand-authored Hindi explanations
            authored = HINDI_DISTRACTORS[qid]
            # Cover all distractor keys, falling back to generic for any
            # not in authored (rare)
            hi_dict = {}
            for k in en_distractors:
                if k in authored:
                    hi_dict[k] = authored[k]
                else:
                    hi_dict[k] = f'इस विकल्प के विवरण के लिए अंग्रेज़ी संस्करण देखें (मूल समीक्षा लंबित)।'
            q['distractor_explanations_hi'] = hi_dict
            q['distractor_explanations_hi_provenance'] = 'llm_curated'
            matched_specific += 1
        else:
            # Generic placeholder for the rest
            hi_dict = {}
            for k, v in en_distractors.items():
                hi_dict[k] = f'यह विकल्प यहाँ अनुपयुक्त है। (अस्थायी; पूर्ण Hindi विवरण की मूल समीक्षा लंबित।)'
            q['distractor_explanations_hi'] = hi_dict
            q['distractor_explanations_hi_provenance'] = 'auto_generated'
            matched_generic += 1

    print(f'Authored distractor_explanations_hi:')
    print(f'  Specific (hand-authored): {matched_specific}')
    print(f'  Generic (auto-generated):  {matched_generic}')
    print(f'  Total:                     {matched_specific + matched_generic}')
    print(f'  Skipped (already had):     {skipped}')

    with QUESTIONS.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == '__main__':
    main()
