def main():
    with QUESTIONS.open('r', encoding='utf-8') as f:
        data = json.load(f)
    qs = data['questions']
    by_id = {q['id']: q for q in qs}
    matched = 0
    skipped = 0
    not_found = []
    for qid, hi in HINDI_EXPLANATIONS.items():
        q = by_id.get(qid)
        if q is None:
            not_found.append(qid)
            continue
        if q.get('explanation_hi'):
            skipped += 1
            continue
        q['explanation_hi'] = hi
        q['explanation_hi_provenance'] = 'llm_curated'
        matched += 1

    # For any questions still missing explanation_hi, fill with a
    # generic Hindi placeholder pointing to the pattern
    generic = 0
    for q in qs:
        if not q.get('explanation_hi'):
            pid = q.get('grammarPatternId', '?')
            q['explanation_hi'] = f'पैटर्न {pid} का प्रश्न — विवरण के लिए संबंधित पैटर्न पृष्ठ देखें। (अस्थायी; मूल समीक्षा प्रतीक्षित।)'
            q['explanation_hi_provenance'] = 'auto_generated'
            generic += 1

    print(f'Authored explanation_hi on {matched} questions (specific Hindi).')
    print(f'Auto-generated placeholder on {generic} questions.')
    print(f'Skipped (already had value): {skipped}')
    if not_found:
        print(f'IDs not found: {len(not_found)}')

    with QUESTIONS.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == '__main__':
    main()
