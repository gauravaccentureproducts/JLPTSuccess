def main():
    with READING.open('r', encoding='utf-8') as f:
        data = json.load(f)
    matched = 0
    skipped = 0
    not_found = []
    by_id = {p['id']: p for p in data['passages']}
    for pid, summary_hi in SUMMARIES.items():
        p = by_id.get(pid)
        if p is None:
            not_found.append(pid)
            continue
        if p.get('summary_hi'):
            skipped += 1
            continue
        p['summary_hi'] = summary_hi
        p['summary_hi_provenance'] = 'llm_curated'
        matched += 1
    print(f'Authored summary_hi on {matched} passages.')
    print(f'Skipped (already had value): {skipped}')
    if not_found:
        print(f'IDs not found: {not_found}')
    with READING.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == '__main__':
    main()
