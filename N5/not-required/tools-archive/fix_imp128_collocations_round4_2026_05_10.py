vocab_path = ROOT / 'data' / 'vocab.json'
data = json.loads(vocab_path.read_text(encoding='utf-8'))
entries = data['entries']

updated = 0
for e in entries:
    form = e.get('form')
    if form not in COLLOCATIONS:
        continue
    new_collocs = COLLOCATIONS[form]
    existing = e.get('collocations') or []
    for c in new_collocs:
        if c not in existing:
            existing.append(c)
    e['collocations'] = existing
    e['collocations_provenance'] = 'llm_curated'
    updated += 1

vocab_path.write_text(
    json.dumps(data, ensure_ascii=False, indent=2) + '\n',
    encoding='utf-8',
)

# Verify N5 kanji compliance
kdata = json.loads((ROOT / 'data' / 'kanji.json').read_text(encoding='utf-8'))
kentries = kdata.get('entries', kdata) if isinstance(kdata, dict) else kdata
whitelist = set()
for k in kentries:
    g = k.get('glyph') or (k.get('id', '').split('.')[-1])
    if g:
        whitelist.add(g)

oos_in_collocs = []
for e in entries:
    if not e.get('collocations'):
        continue
    for c in e['collocations']:
        for ch in c:
            if '一' <= ch <= '鿿' and ch not in whitelist:
                oos_in_collocs.append((e.get('form'), c, ch))
                break

CONTENT_POS = {'noun','verb-1','verb-2','verb-3','i-adj','na-adj','adverb'}
PARTICLE_LIKE = {'は','が','を','に','へ','で','と','から','まで','の','も','や','か','ね','よ','って','なあ','ので','のに','けど','けれど','けれども','て','よね'}
candidates = [e for e in entries if e.get('pos') in CONTENT_POS and e.get('frequency_rank') and e.get('form') not in PARTICLE_LIKE and e.get('reading') not in PARTICLE_LIKE and len(e.get('form') or '') >= 2]
candidates.sort(key=lambda e: e['frequency_rank'])
top300 = candidates[:300]
at5 = sum(1 for e in top300 if len(e.get('collocations') or []) >= 5)
print(f'Vocab entries updated: {updated}')
print(f'Top-300 >=5 coverage: {at5}/300 ({100*at5/300:.0f}%)')
print(f'Out-of-scope kanji in collocations: {len(oos_in_collocs)}')
if oos_in_collocs[:5]:
    for form, c, ch in oos_in_collocs[:5]:
        print(f'  {form} : {c} (oos={ch})')
