def main():
    with open(GRAMMAR, "r", encoding="utf-8") as f:
        data = json.load(f)

    changed = []
    for p in data["patterns"]:
        pid = p["id"]
        if pid in FIXES:
            old = p.get("meaning_ja", "")
            new = FIXES[pid]
            if old != new:
                p["meaning_ja"] = new
                changed.append((pid, p.get("pattern", ""), new))

    with open(GRAMMAR, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"Updated meaning_ja on {len(changed)} patterns.")
    print(f"Dict size: {len(FIXES)}.")
    print()
    print("Sample:")
    for pid, pat, new in changed[:3]:
        print(f"  {pid}  pattern={pat}")
        print(f"    {new}")
        print()


if __name__ == "__main__":
    main()
