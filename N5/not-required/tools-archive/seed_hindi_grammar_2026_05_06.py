def main():
    with GRAMMAR.open('r', encoding='utf-8') as f:
        data = json.load(f)

    patterns = data['patterns']
    matched_meaning = 0
    skipped_meaning = 0
    matched_l1 = 0
    skipped_l1 = 0

    for p in patterns:
        pid = p.get('id')

        # 1. meaning_hi
        if p.get('meaning_hi'):
            skipped_meaning += 1
        elif pid in HINDI_MEANING:
            p['meaning_hi'] = HINDI_MEANING[pid]
            matched_meaning += 1
            # Mark provenance
            mp = p.get('meaning_provenance') or {}
            if isinstance(mp, dict):
                mp['hi'] = 'llm_curated'
                p['meaning_provenance'] = mp

        # 2. l1_notes (only for patterns with a Hindi-specific note)
        if pid in HINDI_L1_NOTES:
            existing = p.get('l1_notes')
            if isinstance(existing, dict) and existing:
                # Merge: only add the keys not already present
                for k, v in HINDI_L1_NOTES[pid].items():
                    if k not in existing:
                        existing[k] = v
                p['l1_notes'] = existing
                matched_l1 += 1
            else:
                p['l1_notes'] = dict(HINDI_L1_NOTES[pid])
                matched_l1 += 1
        else:
            if isinstance(p.get('l1_notes'), dict):
                skipped_l1 += 1

    # Update _translation_status to track Hindi seed
    ts = data.get('_translation_status') or {}
    if isinstance(ts, dict):
        ts.setdefault('translatedPerLocale', {})
        # Track meaning_hi count, not explanation_hi (which remains 0
        # per the native-review-only policy for the long field)
        ts['meaning_hi_seeded'] = sum(1 for p in patterns if p.get('meaning_hi'))
        ts['l1_notes_hindi_count'] = sum(
            1 for p in patterns
            if isinstance(p.get('l1_notes'), dict) and p['l1_notes']
        )

    print(f'meaning_hi: matched and seeded = {matched_meaning}, '
          f'already had = {skipped_meaning}, '
          f'in seed dict but no match = '
          f'{len(set(HINDI_MEANING) - {p.get("id") for p in patterns})}')
    print(f'l1_notes Hindi: applied to {matched_l1} patterns; '
          f'patterns without Hindi-applicable L1 notes (kept empty): {skipped_l1}')

    with GRAMMAR.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'Wrote: {GRAMMAR}')


if __name__ == '__main__':
    main()
