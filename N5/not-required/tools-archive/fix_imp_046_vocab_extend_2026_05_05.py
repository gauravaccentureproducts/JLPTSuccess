        if key not in TRANSLATIONS:
            continue
        vi, id_, ne, zh = TRANSLATIONS[key]
        e['gloss_vi'] = vi
        e['gloss_id'] = id_
        e['gloss_ne'] = ne
        e['gloss_zh'] = zh
        e['gloss_provenance'] = 'machine_translated'
        n_done += 1
        matched_keys.add(key)

