    # so cross-section vocab (e.g., 月 in §10 and §11) is consistently tagged.
    seen = {}
    for e in data.get('entries', []):
        key = (e.get('form', ''), e.get('reading', ''))
        if key in TRANSLATIONS:
            seen.setdefault(key, []).append(e)
    for key, entries in seen.items():
        if len(entries) > 1:
            vi, id_, ne, zh = TRANSLATIONS[key]
            for e in entries:
                if 'gloss_vi' not in e:
                    e['gloss_vi'] = vi; e['gloss_id'] = id_
                    e['gloss_ne'] = ne; e['gloss_zh'] = zh
                    e['gloss_provenance'] = 'machine_translated'
                    n_done += 1

    # Detect translation keys with no match in vocab.json (typos / wrong reading)
    unmatched = sorted(k for k in TRANSLATIONS if k not in matched_keys)
    if unmatched:
        print('Unmatched translation keys (likely already covered or wrong reading):')
        for k in unmatched[:30]:
            print(f'  {k}')
        if len(unmatched) > 30:
            print(f'  ... and {len(unmatched) - 30} more')

    VOCAB.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
    total = len(data.get('entries', []))
    n_total_translated = sum(1 for e in data.get('entries', []) if 'gloss_vi' in e)
    print(f'\nThis pass: applied {n_done} new translations, skipped {n_skipped} already-done.')
    print(f'Total: {n_total_translated}/{total} entries translated ({100*n_total_translated/total:.1f}% coverage).')
    return 0


