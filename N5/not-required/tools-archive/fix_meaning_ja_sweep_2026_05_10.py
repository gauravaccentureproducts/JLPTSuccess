def main():
    with open(GRAMMAR, "r", encoding="utf-8") as f:
        data = json.load(f)

    changed = []
    skipped = []
    for p in data["patterns"]:
        pid = p["id"]
        if pid in FIXES:
            old = p.get("meaning_ja", "")
            new = FIXES[pid]
            if old != new:
                p["meaning_ja"] = new
                changed.append((pid, p.get("pattern", ""), old, new))
            else:
                skipped.append((pid, "no change"))

    with open(GRAMMAR, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"Updated meaning_ja on {len(changed)} patterns.")
    print(f"Dict had {len(FIXES)} entries; {len(skipped)} were unchanged.")
    print()
    print("Sample diffs (first 5):")
    for pid, pat, old, new in changed[:5]:
        print(f"  {pid}  pattern={pat!r}")
        print(f"    OLD: {old!r}")
        print(f"    NEW: {new}")


if __name__ == "__main__":
    main()
