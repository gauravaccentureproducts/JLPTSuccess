def main():
    with GRAMMAR.open('r', encoding='utf-8') as f:
        data = json.load(f)
    patterns = data['patterns']
    by_id = {p['id']: p for p in patterns}
    matched = 0
    skipped = 0
    not_found = []
    for pid, hi_text in EXPLANATIONS_HI.items():
        p = by_id.get(pid)
        if p is None:
            not_found.append(pid)
            continue
        if p.get('explanation_hi'):
            skipped += 1
            continue
        p['explanation_hi'] = hi_text
        # Add provenance dict
        ep = p.get('explanation_provenance')
        if isinstance(ep, dict):
            ep['hi'] = 'llm_curated'
        elif isinstance(ep, str):
            p['explanation_provenance'] = {'en': ep, 'hi': 'llm_curated'}
        else:
            p['explanation_provenance'] = {'en': 'native_reviewed', 'hi': 'llm_curated'}
        matched += 1

    # Update _translation_status
    ts = data.get('_translation_status') or {}
    if isinstance(ts, dict):
        explanation_hi_count = sum(1 for p in patterns if p.get('explanation_hi'))
        ts.setdefault('translatedPerLocale', {})
        ts['translatedPerLocale']['hi'] = explanation_hi_count
        # Update note (preserve native-review-only history but add LLM-persona caveat)
        existing_note = ts.get('note', '')
        ts['note'] = (
            existing_note +
            ' 2026-05-07 ISSUE-093 update: LLM-persona scaling authorized via Q33; '
            'authored 151 explanation_hi entries with provenance.hi = llm_curated. '
            'Native-review remains the target end-state.'
        )

    print(f'Authored explanation_hi on {matched} patterns.')
    print(f'Skipped (already had value): {skipped}')
    if not_found:
        print(f'IDs not found: {not_found}')

    with GRAMMAR.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


if __name__ == '__main__':
    main()
